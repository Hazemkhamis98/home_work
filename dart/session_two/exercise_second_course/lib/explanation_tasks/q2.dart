/*
Describe the difference between var, dynamic, and explicitly typed variables in Dart.
*/

/*
dynamic
is a variable that we can give it any data type and we can when we give it a datatype change the datatype later 

var
It is not a data type, it is a keyword that lets Dart infer the type from the first assigned value and we  can't change the data type later


explicitly typed variables
An explicitly typed variable is a variable where you write the data type yourself (e.g., int, String, bool).
Once you declare it with a specific type, you cannot change its type later.
*/
